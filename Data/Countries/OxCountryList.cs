        private class CountryComparer : Comparer<OxCountry>
        {
            public override int Compare(OxCountry? x, OxCountry? y) => 
                x is null
                    ? -1
                    : y is null
                        ? 1
                        : x.Name.CompareTo(y.Name);
        }

        private static void AddCountry(string name, string alpha2, string alpha3, int iso, OxCountryLocation location, Bitmap? flag = null,
            string? shortName = null, string? fullName = null, bool isPsn= false)
        {
            string strIso = iso.ToString();

            while (strIso.Length < 3)
                strIso = $"0{strIso}";

            countries.Add(
                new OxCountry()
                {
                    Name = name,
                    Alpha2 = alpha2,
                    Alpha3 = alpha3,
                    ISO = strIso,
                    Location = location,
                    Flag = flag,
                    ShortName = shortName ?? string.Empty,
                    FullName = fullName ?? string.Empty,
                    IsPSN = isPsn
                }
            );
        }

        public static OxCountry? GetCountry(OxCountryField field, object value) => 
            value switch
            {
                null => null,
                _ => Countries.Find((c) => value.Equals(c[field]))
            };

        public static List<OxCountry> Countries => countries;

        public static List<OxCountry> GetCountries(OxCountryField field, object value) =>
            countries.FindAll(c => value.Equals(c[field]));

        private static readonly List<OxCountry> countries = new();
    }
